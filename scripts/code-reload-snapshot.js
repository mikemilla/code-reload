#!/usr/bin/env node
/**
 * code-reload-snapshot — generate a build-time snapshot of an app's source tree.
 *
 * Scans a source directory (the live working tree, including uncommitted
 * changes) and emits a `{ "<relative path>": "<contents>" }` map that
 * `registerCodeReloadApp({ sources })` hands to native for on-device live editing.
 *
 * Usage:
 *   node scripts/code-reload-snapshot.js --src src --out src/appSources.ts [--var appSources]
 */
const fs = require('fs');
const path = require('path');

function parseArgs(argv) {
  const args = {src: 'src', out: 'src/appSources.ts', var: 'appSources'};
  for (let i = 2; i < argv.length; i++) {
    const a = argv[i];
    if (a === '--src') args.src = argv[++i];
    else if (a === '--out') args.out = argv[++i];
    else if (a === '--var') args.var = argv[++i];
  }
  return args;
}

const EXTENSIONS = new Set(['.tsx', '.ts', '.jsx', '.js', '.json']);
const IGNORE_DIRS = new Set(['node_modules', '.git', 'ios', 'android', 'Pods', 'lib', 'build']);

function walk(dir, baseDir, out) {
  for (const entry of fs.readdirSync(dir, {withFileTypes: true})) {
    if (entry.name.startsWith('.')) continue;
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      if (IGNORE_DIRS.has(entry.name)) continue;
      walk(full, baseDir, out);
    } else if (EXTENSIONS.has(path.extname(entry.name))) {
      const rel = path.relative(baseDir, full).split(path.sep).join('/');
      out[rel] = fs.readFileSync(full, 'utf8');
    }
  }
}

function main() {
  const args = parseArgs(process.argv);
  const srcDir = path.resolve(process.cwd(), args.src);
  if (!fs.existsSync(srcDir)) {
    console.error(`code-reload-snapshot: source dir not found: ${srcDir}`);
    process.exit(1);
  }

  const files = {};
  walk(srcDir, srcDir, files);

  const outPath = path.resolve(process.cwd(), args.out);
  fs.mkdirSync(path.dirname(outPath), {recursive: true});

  const header = '// AUTO-GENERATED by code-reload-snapshot. Do not edit by hand.\n';
  const body = `export const ${args.var}: Record<string, string> = ${JSON.stringify(
    files,
    null,
    2,
  )};\n`;
  fs.writeFileSync(outPath, header + body);

  console.log(
    `code-reload-snapshot: wrote ${Object.keys(files).length} files to ${args.out}`,
  );
}

main();
